export function AdminSidebar() {
  const pathname = usePathname();

  return (
    <aside className="fixed inset-y-0 left-0 w-56 border-r border-slate-200 bg-white">
      <div className="flex h-14 items-center border-b border-slate-200 px-4">
        <Link href="/admin/" className="text-sm font-bold text-slate-900">
          ToolAlts Admin
        </Link>
      </div>
      <nav className="mt-2 space-y-0.5 px-2">
        {NAV_ITEMS.map((item) => {
          const isActive =
            item.href === "/admin/"
              ? pathname === "/admin/"
              : pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-2.5 rounded-lg px-3 py-2 text-sm transition ${
                isActive
                  ? "bg-indigo-50 font-medium text-indigo-700"
                  : "text-slate-600 hover:bg-slate-50 hover:text-slate-900"
              }`}
            >
              <NavIcon type={item.icon} />
              {item.label}
            </Link>
          );
        })}
      </nav>
      <div className="absolute bottom-0 left-0 right-0 border-t border-slate-200 p-3">
        <form
          action="/api/admin/auth/"
          method="DELETE"
          onSubmit={async (e) => {
            e.preventDefault();
            await fetch("/api/admin/auth/", { method: "DELETE" });
            window.location.href = "/admin/login/";
          }}
        >
          <button
            type="submit"
            className="w-full rounded-lg px-3 py-1.5 text-left text-xs text-slate-500 transition hover:bg-slate-50 hover:text-slate-700"
          >
            Sign out
          </button>
        </form>
      </div>
    </aside>
  );
}
